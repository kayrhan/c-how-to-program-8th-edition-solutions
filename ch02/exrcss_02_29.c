// This program finds some characters' integer equivalents.

#include <stdio.h>

int main(void) { // function main begins program execution
	printf("A's integer equivalent is %d\n", 'A'); // A
	printf("B's integer equivalent is %d\n", 'B'); // B
	printf("C's integer equivalent is %d\n", 'C'); // C
	printf("a's integer equivalent is %d\n", 'a'); // a
	printf("b's integer equivalent is %d\n", 'b'); // b
	printf("c's integer equivalent is %d\n", 'c'); // c
	printf("0's integer equivalent is %d\n", '0'); // 0
	printf("1's integer equivalent is %d\n", '1'); // 1
	printf("2's integer equivalent is %d\n", '2'); // 2
	printf("$'s integer equivalent is %d\n", '$'); // $
	printf("*'s integer equivalent is %d\n", '*'); // *
	printf("+'s integer equivalent is %d\n", '+'); // +
	printf("/'s integer equivalent is %d\n", '/'); // (/)
	printf(" 's integer equivalent is %d\n", ' '); // (blank space)
} // end function main
